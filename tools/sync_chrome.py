HEAD_START = "<!-- chrome:head (generated by tools/sync_chrome.py; edit there, then re-run) -->"
HEAD_END = "<!-- /chrome:head -->"
NAV_START = "<!-- chrome:nav (generated by tools/sync_chrome.py; edit there, then re-run) -->"
NAV_END = "<!-- /chrome:nav -->"
FOOT_START = "<!-- chrome:footer (generated by tools/sync_chrome.py; edit there, then re-run) -->"
FOOT_END = "<!-- /chrome:footer -->"

SKIP_LINK = '<a href="#main-content" class="skip-link">Skip to main content</a>'


def head_html(prefix):
    """Shared head boilerplate: theme colors, icons, styles, scripts."""
    lines = [
        # Address-bar color for mobile browsers; theme.js re-syncs these two
        # when a manual theme toggle overrides the OS preference.
        '<meta name="theme-color" content="#f5f5f7" media="(prefers-color-scheme: light)">',
        '<meta name="theme-color" content="#000000" media="(prefers-color-scheme: dark)">',
        '<link rel="icon" type="image/png" href="{0}img/favicon.png">'.format(prefix),
        '<link rel="apple-touch-icon" href="{0}img/favicon.png">'.format(prefix),
    ]
    lines += [
        '<link rel="stylesheet" href="{0}css/{1}.css">'.format(prefix, name)
        for name in CSS_PARTIALS
    ]
    lines += [
        # theme.js stays synchronous so data-theme is set before first paint.
        '<script src="{0}js/theme.js"></script>'.format(prefix),
        '<script src="{0}js/layout.js" defer></script>'.format(prefix),
    ]
    return "\n    ".join(lines)


def nav_html(page, prefix):
    active = ACTIVE_ALIASES.get(page, page)
    links = "".join(
        '<a href="{0}{1}"{3}>{2}</a>'.format(
            prefix, href, label, ' class="active"' if href == active else ""
        )
        for href, label in NAV_LINKS
    )
    return (
        '<nav aria-label="Primary navigation">'
        + links
        + '<span class="sep">|</span>'
        + '<button id="theme-toggle" onclick="toggleTheme()" class="theme-toggle-btn" aria-label="Toggle dark mode"></button>'
        + "</nav>"
    )


def footer_html(prefix):
    links = '<span class="sep">|</span>'.join(
        '<a href="{0}{1}">{2}</a>'.format(prefix, href, label) for href, label in NAV_LINKS
    )
    return (
        "<footer>"
        + '<div class="footer-socials">'
        + '<a href="{0}" target="_blank" rel="noopener noreferrer" aria-label="GitHub">{1}</a>'.format(GITHUB_URL, GITHUB_SVG)
        + '<a href="{0}" target="_blank" rel="noopener noreferrer" aria-label="LinkedIn">{1}</a>'.format(LINKEDIN_URL, LINKEDIN_SVG)
        + "</div>"
        + "<p>&copy; 2026 Kevin Struna. All rights reserved.</p>"
        + '<div class="footer-links">' + links + "</div>"
        + "</footer>"
    )


# Any local stylesheet/script reference, with or without an existing stamp.
ASSET_REF = re.compile(r'((?:href|src)=")(/?)((?:css|js)/[A-Za-z0-9_.-]+)(?:\?v=[0-9a-f]{8})?(")')


def asset_version(path):
    with open(path, "rb") as f:
        return hashlib.md5(f.read()).hexdigest()[:8]


def stamp_assets(html):
    """Append ?v=<content hash> to every local css/js URL (idempotent)."""
    return ASSET_REF.sub(
        lambda m: "{0}{1}{2}?v={3}{4}".format(
            m.group(1), m.group(2), m.group(3), asset_version(m.group(3)), m.group(4)
        ),
        html,
    )


def replace_block(html, start, end, block, legacy_placeholder=None):
    """Insert the generated block, replacing either the legacy JS-mount
    placeholder (first migration) or a previously generated block (re-runs)."""
    if legacy_placeholder and legacy_placeholder in html:
        return html.replace(legacy_placeholder, block)
    pattern = re.escape(start) + r".*?" + re.escape(end)
    if re.search(pattern, html, flags=re.S):
        return re.sub(pattern, block, html, flags=re.S)
    raise SystemExit("no chrome mount point found (marker or placeholder)")


def main():
    os.chdir(os.path.join(os.path.dirname(os.path.abspath(__file__)), ".."))
    for path in sorted(glob.glob("*.html")):
        page = os.path.basename(path)
        prefix = URL_PREFIXES.get(page, "")
        with open(path, encoding="utf-8") as f:
            html = f.read()

        head_block = "{0}\n    {1}\n    {2}".format(HEAD_START, head_html(prefix), HEAD_END)
        nav_block = "{0}\n    {1}\n    {2}".format(NAV_START, nav_html(page, prefix), NAV_END)
        foot_block = "{0}\n    {1}\n    {2}".format(FOOT_START, footer_html(prefix), FOOT_END)
        html = replace_block(html, HEAD_START, HEAD_END, head_block)
        html = replace_block(html, NAV_START, NAV_END, nav_block, '<div id="site-nav"></div>')
        html = replace_block(html, FOOT_START, FOOT_END, foot_block, '<div id="site-footer"></div>')

        # Static skip link + focus target (previously added by layout.js).
        if 'class="skip-link"' not in html:
            html = html.replace("<body>", "<body>\n\n    " + SKIP_LINK, 1)
        html = html.replace("<main>", '<main id="main-content" tabindex="-1">', 1)

        html = stamp_assets(html)

        with open(path, "w", encoding="utf-8") as f:
            f.write(html)
        print("synced", page)


if __name__ == "__main__":
    main()
