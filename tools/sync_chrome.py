"""
Inline the shared site chrome (head boilerplate, skip link, nav, footer)
statically into every page, so the site works without JavaScript and the
header doesn't pop in after page load.

This script is the single source of truth for the shared <head> block (theme
colors, icons, stylesheet links in cascade order, script tags), the nav, and
the footer. After changing NAV_LINKS, CSS_PARTIALS, or anything else here,
re-run from the repo root:

    python3 tools/sync_chrome.py

It rewrites the blocks between the chrome markers in each *.html file
(idempotent; safe to run repeatedly), then stamps every local css/js URL
with a short content hash (?v=...) so a changed asset always gets a fresh
URL. Cloudflare fronts the site with a 4-hour browser cache, and without
the stamp a visitor can hold yesterday's stylesheet against today's HTML.
layout.js keeps only the behavior that genuinely needs JS (back-to-top
button, video lightbox).
"""
import glob
import hashlib
import os
import re

NAV_LINKS = [
    ("index.html", "Home"),
    ("projects.html", "Projects"),
    ("about.html", "About"),
    ("resume.html", "Resume"),
    ("contact.html", "Contact"),
    ("blog.html", "Blog"),
    ("uses.html", "Uses"),
]

# Pages that should highlight a nav item other than themselves.
ACTIVE_ALIASES = {
    "apple-developer-fee.html": "blog.html",
    "kanacard.html": "projects.html",
}

# 404.html is served at whatever URL the visitor requested, so its chrome
# URLs must be root-absolute. (Not <base href="/">: that would also rebase
# same-page #fragment links, sending the skip link to the homepage.)
URL_PREFIXES = {"404.html": "/"}

# Stylesheet partials, linked in cascade order (later files override earlier).
CSS_PARTIALS = ["base", "layout", "components", "pages", "utilities", "themes", "responsive"]

